//4 math functions for addition, subtraction, multiplication, and divison

function add(a, b) {
    return a + b;
};

function subtract(a, b) {
    return a - b;
};

function multiply(a, b) {
    return a * b;
};

function divide(a, b) {
    return a / b;
};

module.exports = { add, subtract, multiply, divide};